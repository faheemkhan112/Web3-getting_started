var personName2 = "\t\n   John Doe   \n\t";
// Print name with whitespace displayed
console.log("Name with whitespace: \"".concat(personName2, "\""));
// Strip whitespace from the name
var strippedName = personName2.trim();
// Print name after stripping whitespace
console.log("Name after stripping whitespace: \"".concat(strippedName, "\""));
